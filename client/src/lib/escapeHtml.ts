/**
 * Escapes HTML special characters to prevent XSS when interpolating
 * values into HTML strings (document.write PDFs, print exports, etc.).
 */
export function escapeHtml(str: string | null | undefined): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#x27;');
}
